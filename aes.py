xtime = lambda a: (((a << 1) ^ 0x11B) & 0xFF) if (a & 0x80) else (a << 1)

def bytes2matrix(text: bytes) -> List[List[int]]:
    matrix = []
    for i in range(4):
        tmp = []
        for j in range(4):
            tmp.append(text[i*4+j])
        matrix.append(tmp)
    return matrix


def matrix2bytes(matrix: List[List[int]]) -> bytes:
    text = bytearray()
    for i in range(4):
        for j in range(4):
            text += long2bytes(matrix[i][j])
    return bytes(text)


class AES:
    def __init__(self, primary_key: bytes, mode: Literal['ECB', 'CBC'] = 'ECB',iv: bytes = b'\x00' * 16):
        '''
        AES-128初始化 传入16字节的密钥primary_key
        使用扩展密钥算法生成轮密钥round_keys
        '''
        if len(primary_key) != 16:
            raise Exception('key length error')
        if mode not in ['ECB', 'CBC']:
            raise Exception('mode error')
        self.mode = mode
        self.iv = iv
        self.round_keys = self.key_expansion(primary_key)

    def key_expansion(self, primary_key: bytes) -> List[List[int]]:
        '''
        对密钥primary_key进行密钥扩展 生成轮密钥round_keys
        '''
        def rotl(array):
            # 字循环
            return array[1:] + array[:1]
        
        # 初始轮密钥
        round_keys = bytes2matrix(primary_key)
        for i in range(4, 44):
            round_keys.append([])
            if i % 4 == 0:
                # w[i] = w[i-4] ^ G(W[i-1])
                rotl_array = rotl(round_keys[i - 1]) # 字循环
                for j in range(4):
                    byte = round_keys[i - 4][j] ^ S_Box[rotl_array[j]] # 异或 字节代换
                    round_keys[i].append(byte)
                round_keys[i][0] ^= Rcon[i // 4]   # 异或轮常量
            else:
                # w[i] = w[i-4] ^ w[i-1]
                for j in range(4):
                    byte = round_keys[i - 4][j] ^ round_keys[i - 1][j]
                    round_keys[i].append(byte)
        return round_keys

    def encrypt(self, message: bytes) -> bytes:
        '''
        加密明文message
        '''
        if len(message) % 16 != 0:
            raise Exception('message length error')

        encrypt_res = b''
        for i in range(len(message) // 16):
            text = message[i * 16: i * 16 + 16]
            if self.mode == 'CBC':
                if i == 0:
                    text = xor_bytes(text, self.iv)
                else:
                    text = xor_bytes(text, encrypt_res[(i - 1) * 16: i * 16])
            matrix = bytes2matrix(text)
            self.add_round_key(matrix, self.round_keys[:4])  # 初始轮密钥加 
            for j in range(1, 10):
                self.round_encrypt(matrix, self.round_keys[4 * j : 4 * j + 4])  # 轮函数
            # 最后一轮 没有列混淆
            self.sub_byte(matrix)
            self.shift_row(matrix)
            self.add_round_key(matrix, self.round_keys[40:])
            encrypt_res += matrix2bytes(matrix)
        return encrypt_res

    def decrypt(self, message: bytes) -> bytes:
        '''
        解密密文message
        '''
        if len(message) % 16 != 0:
            raise Exception('message length error')
        
        decrypt_res = b''
        for i in range(len(message) // 16):
            matrix = bytes2matrix(message[i * 16: i * 16 + 16])
            self.add_round_key(matrix, self.round_keys[40:])
            self.inv_shift_row(matrix)
            self.inv_sub_byte(matrix)
            for j in range(9, 0 ,-1):
                self.round_decrypt(matrix, self.round_keys[4 * j : 4 * j + 4])
            self.add_round_key(matrix, self.round_keys[:4])
            text = matrix2bytes(matrix)
            if self.mode == 'CBC':
                if i == 0:
                    text = xor_bytes(text, self.iv)
                else:
                    text = xor_bytes(text, message[(i - 1) * 16: i * 16])
            decrypt_res += text
        return decrypt_res

    def add_round_key(self, text: List[List[int]], key: List[List[int]]):
        '''
        轮密钥加
        '''
        for i in range(4):
            for j in range(4):
                text[i][j] ^= key[i][j]

    def round_encrypt(self, matrix: List[List[int]], key: List[List[int]]):
        '''
        轮函数加密 字节代换->行移位->列混淆->轮密钥加
        '''
        self.sub_byte(matrix)
        self.shift_row(matrix)
        self.mix_column(matrix)
        self.add_round_key(matrix, key)

    def round_decrypt(self, matrix: List[List[int]], key: List[List[int]]):
        self.add_round_key(matrix, key)
        self.inv_mix_column(matrix)
        self.inv_shift_row(matrix)
        self.inv_sub_byte(matrix)
    
    def sub_byte(self, matrix):
        for i in range(4):
            for j in range(4):
                matrix[i][j] = S_Box[matrix[i][j]]

    def inv_sub_byte(self, matrix):
        for i in range(4):
            for j in range(4):
                matrix[i][j] = Inv_S_Box[matrix[i][j]]

    def shift_row(self, s):
        s[0][1], s[1][1], s[2][1], s[3][1] = s[1][1], s[2][1], s[3][1], s[0][1]
        s[0][2], s[1][2], s[2][2], s[3][2] = s[2][2], s[3][2], s[0][2], s[1][2]
        s[0][3], s[1][3], s[2][3], s[3][3] = s[3][3], s[0][3], s[1][3], s[2][3]

    def inv_shift_row(self, s):
        s[0][1], s[1][1], s[2][1], s[3][1] = s[3][1], s[0][1], s[1][1], s[2][1]
        s[0][2], s[1][2], s[2][2], s[3][2] = s[2][2], s[3][2], s[0][2], s[1][2]
        s[0][3], s[1][3], s[2][3], s[3][3] = s[1][3], s[2][3], s[3][3], s[0][3]

    def mix_column(self, s):
        for i in range(4):
            a = s[i]
            t = a[0] ^ a[1] ^ a[2] ^ a[3]
            u = a[0]
            a[0] ^= t ^ xtime(a[0] ^ a[1])
            a[1] ^= t ^ xtime(a[1] ^ a[2])
            a[2] ^= t ^ xtime(a[2] ^ a[3])
            a[3] ^= t ^ xtime(a[3] ^ u)
            s[i] = a

    def inv_mix_column(self, s):
        for i in range(4):
            u = xtime(xtime(s[i][0] ^ s[i][2]))
            v = xtime(xtime(s[i][1] ^ s[i][3]))
            s[i][0] ^= u
            s[i][1] ^= v
            s[i][2] ^= u
            s[i][3] ^= v
        s = self.mix_column(s)

